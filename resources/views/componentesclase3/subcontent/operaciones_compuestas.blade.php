        <div class="equation-box">
            <div class="text-center mb-4">
                <span class="math-expression text-2xl">12 ÷ 4 × 3 + 2² - (7 - 3)</span>
            </div>
            
            <div class="space-y-3 mt-6">
                <div class="operation-step">
                    <p class="text-blue-300 font-bold">Paso 1: Resolver paréntesis</p>
                    <p class="text-gray-300"><span class="math-expression">12 ÷ 4 × 3 + 2² - (7 - 3) = 12 ÷ 4 × 3 + 2² - 4</span></p>
                </div>
                
                <div class="operation-step">
                    <p class="text-blue-300 font-bold">Paso 2: Calcular exponentes</p>
                    <p class="text-gray-300"><span class="math-expression">12 ÷ 4 × 3 + 2² - 4 = 12 ÷ 4 × 3 + 4 - 4</span></p>
                </div>
                
                <div class="operation-step">
                    <p class="text-blue-300 font-bold">Paso 3: Multiplicación y división (de izquierda a derecha)</p>
                    <p class="text-gray-300"><span class="math-expression">12 ÷ 4 × 3 + 4 - 4 = 3 × 3 + 4 - 4 = 9 + 4 - 4</span></p>
                </div>
                
                <div class="operation-step">
                    <p class="text-blue-300 font-bold">Paso 4: Suma y resta (de izquierda a derecha)</p>
                    <p class="text-gray-300"><span class="math-expression">9 + 4 - 4 = 13 - 4 = 9</span></p>
                </div>
                
                <div class="bg-blue-900/20 p-4 rounded-lg mt-4">
                    <p class="text-center text-white font-bold">Resultado final: <span class="math-expression text-xl">9</span></p>
                </div>
            </div>
        </div>
    </div>
    
