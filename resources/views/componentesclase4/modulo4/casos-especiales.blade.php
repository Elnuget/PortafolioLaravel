    <h4 class="text-xl font-semibold text-cyan-200 mb-2">Casos Especiales Temporales</h4>
    <p class="text-gray-200">Problemas que combinan diferentes momentos temporales de manera compleja.</p>
    <div class="mt-3 bg-slate-600 bg-opacity-30 p-3 rounded-lg">
        <p class="text-cyan-100">Ejemplo: "Dentro de 15 años, la edad de Patricia será el triple de la que tenía hace 5 años. ¿Cuál es su edad actual?"</p>
        <p class="text-gray-300 mt-2">Sea x = edad actual de Patricia</p>
        <p class="text-gray-300">• Hace 5 años: x - 5</p>
        <p class="text-gray-300">• Dentro de 15 años: x + 15</p>
        <p class="text-gray-300">• Condición: (Edad futura) = 3 × (Edad pasada)</p>
        <p class="text-teal-300 mt-2">x + 15 = 3(x - 5)</p>
        <p class="text-teal-300">x + 15 = 3x - 15</p>
        <p class="text-teal-300">30 = 2x</p>
        <p class="text-teal-300">x = 15 años</p>
        <p class="text-cyan-100 mt-2">Verificación:</p>
        <p class="text-gray-300">• Hace 5 años: 15 - 5 = 10 años</p>
        <p class="text-gray-300">• Dentro de 15 años: 15 + 15 = 30 años</p>
        <p class="text-gray-300">• ¿30 = 3 × 10? ✓ Sí</p>
    </div>
    <div class="mt-3 bg-yellow-900 bg-opacity-20 p-2 rounded text-sm">
        <p class="text-yellow-300">⚠️ Cuidado: Asegúrate de que las edades pasadas sean positivas</p>
