<div class="space-y-8">
    <div>
        <h2 class="text-2xl font-bold text-red-400 mb-4">¿Qué es una Razón?</h2>
        <p class="text-gray-300">
            Una razón es una comparación entre dos cantidades, expresada como una fracción o usando el símbolo ":". Por ejemplo, si en un salón hay 10 niños y 15 niñas, la razón de niños a niñas es 10/15 o 10:15, que se simplifica a 2/3 o 2:3.
        </p>
    </div>

    <div>
        <h2 class="text-2xl font-bold text-red-400 mb-4">¿Qué es una Proporción?</h2>
        <p class="text-gray-300">
            Una proporción es una igualdad entre dos razones. Se utiliza para resolver problemas donde se conoce una razón y se quiere encontrar una cantidad desconocida en otra razón equivalente. Por ejemplo, si 2 de cada 3 estudiantes aprueban un examen, ¿cuántos estudiantes aprobarán en un grupo de 30? La proporción sería 2/3 = x/30.
        </p>
    </div>

    <div>
        <h2 class="text-2xl font-bold text-red-400 mb-4">Ejemplos Prácticos</h2>
        <ul class="list-disc list-inside text-gray-300 space-y-2">
            <li><strong>Recetas de cocina:</strong> Si una receta para 4 personas necesita 2 tazas de harina, ¿cuánta harina se necesita para 6 personas?</li>
            <li><strong>Mapas y escalas:</strong> Si la escala de un mapa es 1 cm : 10 km, ¿cuál es la distancia real entre dos ciudades que están a 5 cm en el mapa?</li>
            <li><strong>Velocidad y distancia:</strong> Si un coche viaja a 60 km/h, ¿cuánto tiempo tardará en recorrer 180 km?</li>
        </ul>
    </div>
</div>
