    <h3 class="text-2xl font-semibold text-red-400 mb-4">Problemas de Aplicación del MCD y MCM</h3>
    <p class="mb-4">Resuelve los siguientes problemas. Identifica si debes usar el MCD o el MCM.</p>
    <ol class="list-decimal list-inside space-y-4 mb-4">
        <li>
            <strong>Problema 1:</strong> Ana tiene 24 caramelos y 18 chocolates. Quiere repartirlos en bolsas de tal forma que cada bolsa tenga la misma cantidad de caramelos y la misma cantidad de chocolates, y que el número de bolsas sea el mayor posible. ¿Cuántas bolsas podrá hacer? ¿Cuántos caramelos y cuántos chocolates tendrá cada bolsa? (<i>Pista: MCD</i>)
        </li>
        <li>
            <strong>Problema 2:</strong> Tres autobuses salen de la misma estación. El primero sale cada 10 minutos, el segundo cada 12 minutos y el tercero cada 15 minutos. Si coincidieron a las 8:00 AM, ¿a qué hora volverán a coincidir los tres autobuses en la estación? (<i>Pista: MCM</i>)
        </li>
        <li>
            <strong>Problema 3:</strong> Se quieren embaldosar una habitación de 420 cm de largo y 300 cm de ancho con baldosas cuadradas, lo más grandes posible y sin cortar ninguna. ¿Cuál debe ser la medida del lado de cada baldosa? ¿Cuántas baldosas se necesitarán? (<i>Pista: MCD</i>)
        </li>
        <li>
            <strong>Problema 4:</strong> Un faro se enciende cada 12 segundos, otro cada 18 segundos y un tercero cada minuto (60 segundos). A las 6:30 p.m. los tres coinciden. Averigua las veces que volverán a coincidir en los cinco minutos siguientes. (<i>Pista: MCM</i>)
        </li>
        <li>
            <strong>Problema 5:</strong> Un comerciante desea poner en cajas 12028 manzanas y 12772 naranjas, de modo que cada caja contenga el mismo número de manzanas o de naranjas y, además, el mayor número posible. Hallar el número de frutas por caja y el número de cajas necesarias. (<i>Pista: MCD</i>)
        </li>
    </ol>
    <details class="bg-gray-800 p-3 rounded-md cursor-pointer">
        <summary class="font-medium text-red-300">Ver Soluciones</summary>
        <ol class="list-decimal list-inside space-y-3 mt-3 text-gray-300">
            <li>
                MCD(24, 18) = 6. Podrá hacer 6 bolsas. Cada bolsa tendrá 24/6 = 4 caramelos y 18/6 = 3 chocolates.
            </li>
            <li>
                MCM(10, 12, 15) = 60. Volverán a coincidir en 60 minutos, es decir, a las 9:00 AM.
            </li>
            <li>
                MCD(420, 300) = 60. El lado de cada baldosa debe ser de 60 cm. Se necesitarán (420/60) * (300/60) = 7 * 5 = 35 baldosas.
            </li>
            <li>
                MCM(12, 18, 60) = 180 segundos = 3 minutos. Coincidirán a las 6:33 p.m. En los siguientes 5 minutos (hasta las 6:35 p.m.), solo coincidirán una vez más a los 3 minutos de la primera coincidencia.
            </li>
            <li>
                MCD(12028, 12772) = 124. Cada caja contendrá 124 frutas. Número de cajas de manzanas = 12028 / 124 = 97. Número de cajas de naranjas = 12772 / 124 = 103. Total de cajas = 97 + 103 = 200 cajas.
            </li>
        </ol>
    </details>
