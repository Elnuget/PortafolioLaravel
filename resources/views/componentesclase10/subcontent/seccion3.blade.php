                    </svg>
                    <p class="text-gray-700">
                        <span class="font-medium">Nota importante:</span> Los cambios porcentuales sucesivos no se suman aritméticamente. En este caso, un aumento del 10% seguido de una disminución del 10% no resulta en un 0% de cambio total.
                    </p>
