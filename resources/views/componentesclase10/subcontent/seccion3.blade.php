        <!-- Ejemplo 1: Descuentos comerciales -->
        <div class="example-block mb-6">
            <h5 class="font-bold text-indigo-800 mb-2">Ejemplo 1: Descuentos Comerciales</h5>
            <p class="text-gray-700 mb-3">Un comerciante ofrece un descuento del 20% en todos sus productos. Si el precio original de un artículo es $350, ¿cuál será el precio final?</p>
            
            <div class="bg-white p-4 rounded-lg border border-gray-200">
                <p class="text-gray-700 mb-2">Paso 1: Calcular el monto del descuento</p>
                <div class="math-formula">
                    \[ \text{Descuento} = 20\% \text{ de } \$350 = 0.20 \times \$350 = \$70 \]
                </div>
                
                <p class="text-gray-700 mb-2 mt-3">Paso 2: Restar el descuento del precio original</p>
                <div class="math-formula">
                    \[ \text{Precio final} = \$350 - \$70 = \$280 \]
                </div>
                
                <p class="text-gray-700 mt-3">También podemos usar la fórmula directa:</p>
                <div class="math-formula">
                    \[ \text{Precio con descuento} = \text{Precio original} \times (1 - \frac{\text{Porcentaje de descuento}}{100}) \]
                    \[ \text{Precio con descuento} = \$350 \times (1 - \frac{20}{100}) = \$350 \times 0.80 = \$280 \]
                </div>
            </div>
        </div>
        
        <!-- Ejemplo 2: Interés Compuesto -->
        <div class="example-block mb-6">
            <h5 class="font-bold text-indigo-800 mb-2">Ejemplo 2: Interés Compuesto</h5>
            <p class="text-gray-700 mb-3">Si inviertes $5,000 a una tasa de interés compuesto del 6% anual, ¿cuánto tendrás después de 3 años?</p>
            
            <div class="bg-white p-4 rounded-lg border border-gray-200">
                <p class="text-gray-700 mb-2">Aplicamos la fórmula del interés compuesto:</p>
                <div class="math-formula">
                    \[ M = P \times (1 + \frac{r}{100})^t \]
                </div>
                <p class="text-gray-700 mb-2">Donde:</p>
                <ul class="list-disc pl-6 text-gray-700 space-y-1 mb-3">
                    <li>M = Monto final</li>
                    <li>P = Principal (cantidad inicial)</li>
                    <li>r = Tasa de interés (en porcentaje)</li>
                    <li>t = Tiempo (en años)</li>
                </ul>
                
                <p class="text-gray-700 mb-2">Sustituimos los valores:</p>
                <div class="math-formula">
                    \[ M = \$5,000 \times (1 + \frac{6}{100})^3 \]
                    \[ M = \$5,000 \times (1.06)^3 \]
                    \[ M = \$5,000 \times 1.191 \]
                    \[ M = \$5,955 \]
                </div>
                
                <p class="text-gray-700 mt-3">Después de 3 años, tendrás $5,955.</p>
            </div>
        </div>
        
        <!-- Ejemplo 3: Concentraciones y Soluciones -->
        <div class="example-block mb-6">
            <h5 class="font-bold text-indigo-800 mb-2">Ejemplo 3: Concentraciones y Soluciones</h5>
            <p class="text-gray-700 mb-3">Una solución de alcohol al 70% contiene 70 ml de alcohol por cada 100 ml de solución. ¿Cuánto alcohol hay en 250 ml de esta solución?</p>
            
            <div class="bg-white p-4 rounded-lg border border-gray-200">
                <p class="text-gray-700 mb-2">Aplicamos la fórmula de porcentaje:</p>
                <div class="math-formula">
                    \[ \text{Cantidad de alcohol} = 70\% \text{ de } 250 \text{ ml} = 0.70 \times 250 \text{ ml} = 175 \text{ ml} \]
                </div>
                
                <p class="text-gray-700 mt-3">Por lo tanto, en 250 ml de solución hay 175 ml de alcohol.</p>
            </div>
        </div>
        
        <!-- Ejemplo 4: Incrementos y Decrementos Porcentuales Sucesivos -->
        <div class="example-block">
            <h5 class="font-bold text-indigo-800 mb-2">Ejemplo 4: Cambios Porcentuales Sucesivos</h5>
            <p class="text-gray-700 mb-3">El precio de un producto aumentó un 10% en enero y luego disminuyó un 10% en febrero. ¿Cuál es el cambio porcentual total?</p>
            
            <div class="bg-white p-4 rounded-lg border border-gray-200">
                <p class="text-gray-700 mb-2">Supongamos un precio inicial P:</p>
                <div class="math-formula">
                    \[ \text{Precio después del aumento} = P \times (1 + \frac{10}{100}) = P \times 1.10 \]
                </div>
                
                <p class="text-gray-700 mb-2 mt-3">Luego aplicamos la disminución del 10%:</p>
                <div class="math-formula">
                    \[ \text{Precio final} = P \times 1.10 \times (1 - \frac{10}{100}) = P \times 1.10 \times 0.90 = P \times 0.99 \]
                </div>
                
                <p class="text-gray-700 mt-3">Esto significa que el precio final es el 99% del precio original, lo que equivale a una disminución del 1%.</p>
                
                <div class="flex items-center p-3 bg-indigo-50 rounded-lg mt-4 text-sm">
                    <svg class="w-5 h-5 text-indigo-600 mr-2 flex-shrink-0" fill="none" stroke="currentColor" viewBox="0 0 24 24">
