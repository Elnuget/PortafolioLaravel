            </svg>
            Ejemplos Detallados
        </h4>
        
        <div class="grid grid-cols-1 md:grid-cols-2 gap-6">
            <div class="example-block">
                <h5 class="font-bold text-indigo-800 mb-2">Ejemplo 1: Cálculo directo</h5>
                <p class="text-gray-700 mb-3">¿Cuánto es el 25% de 80 estudiantes?</p>
                <div class="bg-white p-3 rounded-lg border border-gray-200">
                    <p class="text-gray-700 mb-1">Aplicamos la fórmula:</p>
                    <div class="my-2">
                        \[ 25\% \text{ de } 80 = \frac{25}{100} \times 80 = 0.25 \times 80 = 20 \]
                    </div>
                    <p class="text-gray-700">Por lo tanto, el 25% de 80 estudiantes son 20 estudiantes.</p>
                </div>
            </div>
            
            <div class="example-block">
                <h5 class="font-bold text-indigo-800 mb-2">Ejemplo 2: Descuento</h5>
                <p class="text-gray-700 mb-3">Si un producto cuesta $120 y tiene un descuento del 15%, ¿cuánto pagaré?</p>
                <div class="bg-white p-3 rounded-lg border border-gray-200">
                    <p class="text-gray-700 mb-1">Primero calculamos el valor del descuento:</p>
                    <div class="my-2">
                        \[ \text{Descuento} = 15\% \text{ de } \$120 = \frac{15}{100} \times \$120 = \$18 \]
                    </div>
                    <p class="text-gray-700 mb-1">Ahora restamos del precio original:</p>
                    <div class="my-2">
                        \[ \text{Precio final} = \$120 - \$18 = \$102 \]
                    </div>
                    <p class="text-gray-700">Pagaré $102 por el producto.</p>
                </div>
            </div>
            
            <div class="example-block">
                <h5 class="font-bold text-indigo-800 mb-2">Ejemplo 3: Calcular porcentaje</h5>
                <p class="text-gray-700 mb-3">En un examen, Juan respondió correctamente 18 de 24 preguntas. ¿Qué porcentaje de respuestas correctas obtuvo?</p>
                <div class="bg-white p-3 rounded-lg border border-gray-200">
                    <p class="text-gray-700 mb-1">Aplicamos la fórmula:</p>
                    <div class="my-2">
                        \[ \text{Porcentaje} = \frac{18}{24} \times 100\% = 0.75 \times 100\% = 75\% \]
                    </div>
                    <p class="text-gray-700">Juan obtuvo un 75% de respuestas correctas en el examen.</p>
                </div>
            </div>
            
            <div class="example-block">
                <h5 class="font-bold text-indigo-800 mb-2">Ejemplo 4: Valor original</h5>
                <p class="text-gray-700 mb-3">Si después de un aumento del 20% un salario es de $1,800, ¿cuál era el salario original?</p>
                <div class="bg-white p-3 rounded-lg border border-gray-200">
                    <p class="text-gray-700 mb-1">Aplicamos la fórmula del valor original:</p>
                    <div class="my-2">
                        \[ \text{Salario Original} = \frac{\$1,800}{1 + \frac{20}{100}} = \frac{\$1,800}{1.2} = \$1,500 \]
                    </div>
                    <p class="text-gray-700">El salario original era de $1,500.</p>
                </div>
            </div>
        </div>
    </div>
    
