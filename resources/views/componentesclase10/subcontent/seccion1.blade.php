<!-- Sección 1: Definición de Porcentaje -->
<section id="seccion1" class="mb-12">
    <div class="section-header flex items-center mb-6">
        <div class="w-10 h-10 bg-indigo-600 rounded-lg flex items-center justify-center mr-4 shadow-lg">
            <span class="text-white font-bold">1</span>
        </div>
        <h3 class="text-2xl font-bold text-gray-800">¿Qué es un Porcentaje?</h3>
    </div>
    
    <div class="bg-white bg-opacity-80 p-6 rounded-xl border border-gray-200 shadow-sm mb-6">
        <p class="text-gray-700 mb-4">
            Un <span class="highlight-text">porcentaje</span> es una forma de expresar un número como una fracción de 100. Se representa con el símbolo %. Esto significa "por cada cien" o "de cada cien partes".
        </p>
        
        <div class="math-formula">
            <p class="mb-2 font-medium text-gray-700">Representación matemática de un porcentaje:</p>
            <div class="my-4">
                \[ x\% = \frac{x}{100} \]
            </div>
        </div>
        
        <div class="flex items-center p-4 bg-indigo-50 rounded-lg border-l-4 border-indigo-500 mt-6">
            <svg class="w-6 h-6 text-indigo-600 mr-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13 16h-1v-4h-1m1-4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z"></path>
            </svg>
            <p class="text-gray-700 text-sm font-medium">
                El término proviene del latín "per centum" que significa "por ciento". Es una forma de expresar proporciones de manera estandarizada.
            </p>
        </div>
    </div>
    
    <!-- Fórmula del porcentaje -->
    <div class="mb-6">
        <div class="code-header flex justify-between items-center bg-indigo-100 rounded-t-lg px-4 py-2">
            <span class="text-sm text-indigo-800 font-medium">Fórmulas básicas</span>
        </div>
        <div class="bg-white rounded-b-lg p-6 border border-indigo-100">
            <div class="math-formula mb-4">
                <p class="mb-2 font-medium text-gray-700">1. Para calcular el porcentaje de una cantidad:</p>
                \[ \text{Porcentaje de una cantidad} = \frac{\text{Porcentaje}}{100} \times \text{Cantidad} \]
            </div>
            
            <div class="math-formula mb-4">
                <p class="mb-2 font-medium text-gray-700">2. Para calcular qué porcentaje es una cantidad de otra:</p>
                \[ \text{Porcentaje} = \frac{\text{Parte}}{\text{Total}} \times 100\% \]
            </div>
            
            <div class="math-formula">
                <p class="mb-2 font-medium text-gray-700">3. Para calcular el valor original conociendo un porcentaje:</p>
                \[ \text{Valor Original} = \frac{\text{Valor con porcentaje aplicado}}{\frac{\text{Porcentaje}}{100}} \]
            </div>
        </div>
    </div>
    
    <!-- Ejemplos detallados -->
    <div class="mt-8">
        <h4 class="text-xl font-bold text-gray-800 mb-4 flex items-center">
