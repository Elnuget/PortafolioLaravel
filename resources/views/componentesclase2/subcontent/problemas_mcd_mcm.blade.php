                <h5 class="text-blue-200 font-medium mb-2">Problema 1: La Fiesta de Cumpleaños</h5>
                <div class="text-blue-100 text-sm">
                    <p>48 = 2⁴ × 3, 36 = 2² × 3², 60 = 2² × 3 × 5</p>
                    <p>MCD(48, 36, 60) = 2² × 3 = 12</p>
                    <p><strong>Respuesta:</strong> 12 centros de mesa</p>
                    <p>Cada centro tendrá: 4 globos rojos, 3 azules y 5 amarillos</p>
