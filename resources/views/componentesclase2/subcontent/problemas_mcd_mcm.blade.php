                <h5 class="text-purple-200 font-medium mb-2">Problema 3: La Panadería</h5>
                <div class="text-purple-100 text-sm">
                    <p>144 = 2⁴ × 3², 96 = 2⁵ × 3, 72 = 2³ × 3²</p>
                    <p>MCD(144, 96, 72) = 2³ × 3 = 24</p>
                    <p><strong>Respuesta:</strong> 24 paquetes</p>
                    <p>Cada paquete: 6 panes dulces, 4 croissants, 3 donas</p>
                </div>
            </div>

            <div class="bg-orange-900 p-4 rounded-lg">
                <h5 class="text-orange-200 font-medium mb-2">Problema 4: El Semáforo Inteligente</h5>
