                    <p>45 = 3² × 5, 60 = 2² × 3 × 5, 75 = 3 × 5²</p>
                    <p>MCM(45, 60, 75) = 2² × 3² × 5² = 900 segundos = 15 minutos</p>
                    <p><strong>Respuesta:</strong> A las 3:15 PM</p>
                </div>
