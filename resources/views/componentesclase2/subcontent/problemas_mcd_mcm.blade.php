                    <p>84 = 2² × 3 × 7, 126 = 2 × 3² × 7, 168 = 2³ × 3 × 7</p>
                    <p>MCD(84, 126, 168) = 2 × 3 × 7 = 42</p>
                    <p><strong>Respuesta:</strong> Pedazos de 42 cm cada uno</p>
                    <p>Obtendrá: 2 + 3 + 4 = 9 pedazos en total</p>
                </div>
