function showHint(num) {
    const hintElement = document.getElementById(`hint${num}`);
    hintElement.classList.toggle('hidden');
    
    // Efecto de fade in/out
    if (!hintElement.classList.contains('hidden')) {
        hintElement.style.opacity = '0';
        hintElement.style.transform = 'translateY(-10px)';
        setTimeout(() => {
            hintElement.style.transition = 'all 0.3s ease';
            hintElement.style.opacity = '1';
            hintElement.style.transform = 'translateY(0)';
        }, 10);
    }
}

function normalizeAnswer(answer) {
    return answer
        .toLowerCase()
        .replace(/\s+/g, '')
        .replace(/\*/g, '')
        .replace(/√ó/g, '')
        .replace(/(\d)([a-z])/g, '$1$2') // 3x queda como 3x
        .replace(/([a-z])(\d)/g, '$1$2'); // x3 queda como x3
}

function checkAnswer(inputId, correctAnswer, num) {
    const input = document.getElementById(inputId);
    const userAnswer = input.value.trim();
    const resultDiv = document.getElementById(`result${num}`);
    
    if (!userAnswer) {
        resultDiv.classList.remove('hidden');
        resultDiv.innerHTML = `
            <div class="bg-yellow-900/20 p-3 rounded-lg border border-yellow-500/30">
                <p class="text-yellow-300 font-semibold">‚ö†Ô∏è Por favor, ingresa una respuesta</p>
            </div>
        `;
        return;
    }
    
    const normalizedUser = normalizeAnswer(userAnswer);
    const normalizedCorrect = normalizeAnswer(correctAnswer);
    
    // Respuestas alternativas aceptables
    const validAnswers = {
        1: ['3x+7', '7+3x', '3*x+7', '7+3*x', 'triplex+7', '7+triplex'],
        2: ['20-2x', '20-2*x'],
        3: ['x-5'],
        4: ['x/4-3', '(x/4)-3', 'x√∑4-3'],
        5: ['x+(x+1)+(x+2)', 'x+x+1+x+2', '3x+3', 'x+x+1+x+2', '(x)+(x+1)+(x+2)'],
        6: ['3l+4', '4+3l', '3*l+4', '4+3*l', '3L+4', '4+3L']
    };
    
    const isCorrect = validAnswers[num].some(valid => 
        normalizeAnswer(valid) === normalizedUser
    ) || normalizedCorrect === normalizedUser;
    
    resultDiv.classList.remove('hidden');
    
    if (isCorrect) {
        resultDiv.innerHTML = `
            <div class="bg-green-900/20 p-3 rounded-lg border border-green-500/30 animate-pulse">
                <p class="text-green-300 font-semibold">üéâ ¬°Excelente! ‚úÖ</p>
                <p class="text-gray-300 text-sm">Respuesta correcta: ${correctAnswer}</p>
                <p class="text-green-200 text-sm">Has dominado este tipo de problema</p>
            </div>
        `;
        
        // Efecto de √©xito
        input.classList.add('border-green-400', 'bg-green-900/20');
        setTimeout(() => {
            input.classList.remove('animate-pulse');
        }, 1000);
        
    } else {
        resultDiv.innerHTML = `
            <div class="bg-red-900/20 p-3 rounded-lg border border-red-500/30">
                <p class="text-red-300 font-semibold">‚ùå No es correcto</p>
                <p class="text-gray-300 text-sm">Respuesta esperada: <span class="font-mono bg-gray-700 px-2 py-1 rounded">${correctAnswer}</span></p>
                <p class="text-gray-400 text-sm">Tu respuesta: <span class="font-mono bg-gray-800 px-2 py-1 rounded">${userAnswer}</span></p>
                <button onclick="showHint(${num})" class="mt-2 bg-yellow-600 hover:bg-yellow-700 text-white px-3 py-1 rounded text-sm transition-all">
                    üí° Ver pista
                </button>
            </div>
        `;
        
        // Efecto de error
        input.classList.add('border-red-400', 'bg-red-900/10');
        input.style.animation = 'shake 0.5s ease-in-out';
        setTimeout(() => {
            input.style.animation = '';
            input.classList.remove('border-red-400', 'bg-red-900/10');
        }, 1000);
    }
    
    // Scroll suave hacia el resultado
    setTimeout(() => {
        resultDiv.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    }, 100);
}

