        <p class="font-bold text-red-400">Problema:</p>
        <p class="mt-2">Un estudiante de la EPN necesita una mezcla de ácido sulfúrico al 30% para un experimento. Tiene disponible ácido al 20% y ácido al 50%. Si necesita 100 ml de la mezcla, ¿cuántos ml de cada concentración debe mezclar?</p>
    </div>
    
    <div class="text-gray-200 p-4 bg-gray-900 bg-opacity-50 rounded-lg">
        <p class="font-bold text-red-400">Solución:</p>
        <p class="mt-2">Definimos las variables:</p>
        <ul class="mt-2 space-y-1 ml-4">
            <li>• x = ml de ácido al 20%</li>
            <li>• y = ml de ácido al 50%</li>
        </ul>
        <p class="mt-2">Planteamos el sistema de ecuaciones:</p>
        <ol class="mt-2 space-y-2 ml-4">
            <li>1. Por cantidad total: x + y = 100</li>
            <li>2. Por concentración: 0.20x + 0.50y = 0.30(100)</li>
            <li>3. Simplificando: 0.20x + 0.50y = 30</li>
            <li>4. De la primera ecuación: x = 100 - y</li>
            <li>5. Sustituyendo: 0.20(100 - y) + 0.50y = 30</li>
            <li>6. Resolviendo: 20 - 0.20y + 0.50y = 30</li>
            <li>7. 0.30y = 10 → y = 33.33 ml</li>
            <li>8. x = 100 - 33.33 = 66.67 ml</li>
        </ol>
        <p class="mt-3 font-bold">Respuesta: 66.67 ml de ácido al 20% y 33.33 ml de ácido al 50%.</p>
